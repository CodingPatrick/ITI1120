def pay(p, h):
    if h <= 40:
        return p * h
    elif h > 40 and h <= 60:
        return (p * 40) + (p * 1.5 * (h - 40))
    else:
        return (p * 40) + (p * 1.5 * 20) + (p * 2 * (h - 60))

